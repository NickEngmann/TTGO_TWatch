// Native build implementation for testing
struct BleServer::Impl {
    bool advertising;
    bool connected;
    uint16_t mtu;
    uint8_t recordingState;
    uint8_t connectionStatus;
    uint32_t sequenceNumber;
    BLEConnectionCallback connCallback;
    BLEMTUCallback mtuCallback;
    
    Impl() : advertising(false), connected(false), mtu(23), 
             recordingState(RECORD_STATE_IDLE), connectionStatus(BLE_CONN_DISCONNECTED),
             sequenceNumber(0), connCallback(nullptr), mtuCallback(nullptr) {}
};

BleServer::BleServer() : pImpl(new Impl()) {
    mockConnected = false;
    mockMTU = 23;
    mockRecordingState = RECORD_STATE_IDLE;
    mockConnectionStatus = BLE_CONN_DISCONNECTED;
    mockSequenceNumber = 0;
}

BleServer::~BleServer() {
    if (pImpl) {
        delete pImpl;
        pImpl = nullptr;
    }
}

bool BleServer::init() {
    if (pImpl == nullptr) {
        pImpl = new Impl();
    }
    return true;
}

bool BleServer::advertise(uint16_t interval_min, uint16_t interval_max) {
    if (pImpl == nullptr) {
        return false;
    }
    pImpl->advertising = true;
    return true;
}

void BleServer::stopAdvertise() {
    if (pImpl != nullptr) {
        pImpl->advertising = false;
    }
}

bool BleServer::isAdverting() {
    if (pImpl == nullptr) {
        return false;
    }
    return pImpl->advertising;
}

bool BleServer::isConnected() {
    if (pImpl == nullptr) {
        return false;
    }
    return mockConnected;
}

uint16_t BleServer::getMTU() {
    if (pImpl == nullptr) {
        return 23;
    }
    return mockMTU;
}

bool BleServer::sendChunk(const uint8_t* data, uint16_t size) {
    if (data == nullptr || pImpl == nullptr || !isConnected()) {
        return false;
    }
    
    // Validate size
    if (size == 0 || size > AUDIO_CHUNK_SIZE) {
        size = AUDIO_CHUNK_SIZE;
    }
    
    // Create chunk structure
    AudioChunk_t chunk;
    chunk.chunk_size = size;
    chunk.sequence_number = mockSequenceNumber++;
    chunk.flags = 0;
    
    // Copy data
    memcpy(chunk.chunk_data, data, size);
    
    // In native build, we just validate the chunk was created
    if (chunk.chunk_size > 0 && chunk.chunk_size <= AUDIO_CHUNK_SIZE) {
        return true;
    }
    return false;
}

bool BleServer::sendChunk(AudioChunk_t* chunk) {
    if (chunk == nullptr || !isConnected()) {
        return false;
    }
    
    if (chunk->chunk_size == 0 || chunk->chunk_size > AUDIO_CHUNK_SIZE) {
        return false;
    }
    
    return true;
}

bool BleServer::sendChunkRaw(const uint8_t* data, uint16_t size) {
    return sendChunk(data, size);
}

void BleServer::setRecordingState(uint8_t state) {
    if (pImpl == nullptr) {
        return;
    }
    mockRecordingState = state;
}

uint8_t BleServer::getRecordingState() {
    if (pImpl == nullptr) {
        return RECORD_STATE_IDLE;
    }
    return mockRecordingState;
}

void BleServer::setConnectionStatus(uint8_t status) {
    if (pImpl == nullptr) {
        return;
    }
    mockConnectionStatus = status;
}

uint8_t BleServer::getConnectionStatus() {
    if (pImpl == nullptr) {
        return BLE_CONN_DISCONNECTED;
    }
    return mockConnectionStatus;
}

void BleServer::setConnectionCallback(BLEConnectionCallback callback) {
    if (pImpl == nullptr) {
        return;
    }
    pImpl->connCallback = callback;
}

void BleServer::setMTUCallback(BLEMTUCallback callback) {
    if (pImpl == nullptr) {
        return;
    }
    pImpl->mtuCallback = callback;
}

